export const convertFullMonthString = (integerMonth) => {
  const monthNames = [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December',
  ]
  const monthString = monthNames[integerMonth]
  return monthString
}
